public class Main {
    public static void main(String[] args) {
        System.out.print("Enter number of terms: ");
        int n = 56;

        int a = 0, b = 1;

        System.out.print("Fibonacci Series: ");

        for (int i = 1; i <= n; i++) {
            System.out.print(a + " ");
            int c = a + b;
            a = b;
            b = c;
        }
    }
}
